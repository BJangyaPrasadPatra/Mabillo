        var ajaxCounter = 0;

        function handleSuccess() {
            ajaxCounter++;
            if (ajaxCounter === 1) {
                $('#overlay').fadeOut(500);
            }
        }
        $(document).ready(function() {
            $("#overlay").fadeIn(300);
            GallerList(1);

        });

        function GallerList(id) {
            $.ajax({
                type: "GET",
                url: `${API_URL}/Album/List/Org-Wise/${id}`,
                dataType: "json",
                success: function(response) {
                    // console.log(response)
                    let list = response.data;
                    let rowcont = $('#rowcont');


                    $.each(list, function(index, val) {
                        let card = `
                            <div class="col-6 col-md-4 col-lg-3 isotope-item" data-filter="Category 1">
                               
                                <article class="product"> <a class="product__image-wrap" href="product-content.php?id=${val.id}"><img class="product__image"
                                            src="${val.image_url}" alt="" width="210" height="200" /></a>
                                    <div class="product__main">
                                        <p class="product__title"><a href="product-content.php?id=${val.id}">${val.album_name}</a></p>
                                    </div>
                                </article>
                            </div>
                        `;
                        rowcont.append(card);
                    });
                    handleSuccess()
                },
                complete: function() {
                    $("#overlay").fadeOut(300);
                }
            });
        }
    </script>
