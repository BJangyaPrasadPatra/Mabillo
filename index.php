    function formatDate(dateString) {
      const options = {
        year: 'numeric',
        month: 'long',
        day: 'numeric'
      };
      const date = new Date(dateString);
      return date.toLocaleDateString(undefined, options);
    }


    function getUpdates(skipvalue) {
      let update_cont = $('#update_cont');
      update_cont.empty();

      $.ajax({
        type: "GET",
        url: `${API_URL}/User-Updates/List/${skipvalue}`,
        dataType: "json",
        success: function(response) {
          // console.log(response)
          let list = response.data;
          let count = 0; // Initialize a counter

          $.each(list, function(index, val) {
            if (count < 4) { // Check if the counter is less than 3
              let updatecard = `
              <div class="col-md-3 text-md-right">
            <div class="card p-3 rounded-0">
              <h4 class="mt-3">${val.updates_title}</h4>
              <div class="support_title update_desc">${val.content_desc}</div>
            </div>
          </div>
          `;
              update_cont.append(updatecard);
              count++; // Increment the counter
            } else {
              return false; // Exit the loop if the counter reaches 3
            }
          });
        }
      });
    }
